    private void exibirImagens() {

        int posX = 10, posY = 10, imgPorLinha = 0;
        int LARGURA = this.jPanel2.getWidth();
        //int ALTURA = this.jPanel2.getHeight();
        for (Imagem imagem : listaImagens) {
            BotaoImagePanel botaoImagePanel = new BotaoImagePanel(this.jLabel2, this.jLabel5, this.jLabel6, imagem);
            botaoImagePanel.setBounds(posX, posY, 150, 150);
            botaoImagePanel.setVisible(true);
            this.jPanel2.add(botaoImagePanel);

            imgPorLinha++;
            if (imgPorLinha < 3) {
                posX += 170;
            } else {
                imgPorLinha = 0;
                posY += 155;
                posX = 10;
            }
        }
        this.jPanel2.setPreferredSize(new Dimension(LARGURA, posY + 155));

    }

    private void carregarImagens() {

        try {
            //System.out.println("iniciar carregamento das imagens...");
            ImagemDAO imagemDAO = new ImagemDAO();
            imagemDAO.openConnection();
            this.listaImagens = imagemDAO.findAll();
            imagemDAO.closeConnection();
            //System.out.println("imagens carregadas...");

        } catch (Exception e) {
            System.out.println("Erro no SQL: " + e.getMessage());
        }

    }

